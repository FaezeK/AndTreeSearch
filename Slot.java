package andTreeSearch;

public class Slot {

	String type;
	String day;
	String time;
	int max;
	int min;

	public String getType(){
		return type;
	}
	public String getDay(){
		return day;
	}
	public String getTime(){
		return time;
	}
	public int getMin(){
		return min;
	}
	public int getMax(){
		return max;
	}
}